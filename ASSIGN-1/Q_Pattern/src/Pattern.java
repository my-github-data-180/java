
public class Pattern {

	public static void main(String[] args) {
	
     int x=2;
     for(int i=0;i<x;i++)
     {
    	 for(int j=0;j<x; j++)
    	 {
    		  if(j<x-j-1)
    		  {
    			  System.out.print(" ");
    		  }
    		  else
    		  {
    			  System.out.print("* ");
    		  }
    	 }
    	 System.out.println("");
     }

	}

}
